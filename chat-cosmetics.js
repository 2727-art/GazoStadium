export const CHAT_STANDARD_FRAME_PRODUCTS = Object.freeze(CHAT_FRAME_PRODUCTS.filter((product) => !product.special));
export const CHAT_SPECIAL_FRAME_PRODUCTS = Object.freeze(CHAT_FRAME_PRODUCTS.filter((product) => product.special));
export const CHAT_COSMETIC_PRODUCTS = Object.freeze([...CHAT_BACKGROUND_PRODUCTS, ...CHAT_FRAME_PRODUCTS]);

const PRODUCT_BY_ID = new Map(CHAT_COSMETIC_PRODUCTS.map((product) => [product.id, product]));

export function getChatCosmeticProduct(productId, type = "") {
  const product = PRODUCT_BY_ID.get(String(productId || ""));
  return product && (!type || product.type === type) ? product : null;
}

export function getEquippedChatCosmetics(economy) {
  const inventory = economy?.inventory && typeof economy.inventory === "object" ? economy.inventory : {};
  const equipped = economy?.equipped && typeof economy.equipped === "object" ? economy.equipped : {};
  const frame = getChatCosmeticProduct(equipped.chatFrame, "chatFrame");
  const background = getChatCosmeticProduct(equipped.chatBackground, "chatBackground");
  return {
    chatFrameId: frame && inventory[frame.id] === true ? frame.id : "",
    chatBackgroundId: background && inventory[background.id] === true ? background.id : "",
  };
}

export function chatCosmeticClassNames(chatFrameId, chatBackgroundId) {
  const frame = getChatCosmeticProduct(chatFrameId, "chatFrame");
  const background = getChatCosmeticProduct(chatBackgroundId, "chatBackground");
  const classes = [];
  if (frame || background) classes.push("chat-cosmetic-bubble");
  if (frame) classes.push(frame.className);
  if (background) classes.push(background.className);
  return classes.join(" ");
}
